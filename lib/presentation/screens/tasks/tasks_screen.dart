  Widget _buildTasksList(TaskState taskState, ThemeData theme) {
    if (taskState.isLoading) {
      return _buildLoadingState();
    }

    if (taskState.error != null) {
      return _buildErrorState(taskState.error!, theme);
    }

    final filteredTasks = _getFilteredTasks(taskState.tasks);

    if (filteredTasks.isEmpty) {
      return _buildEmptyState(theme);
    }

    return RefreshIndicator(
      onRefresh: () async {
        await ref.read(taskProvider.notifier).loadTasks();
      },
      child: ListView.builder(
        padding: const EdgeInsets.all(16),
        itemCount: filteredTasks.length,
        itemBuilder: (context, index) {
          final task = filteredTasks[index];
          return FadeTransition(
            opacity: _fadeAnimation,
            child: TaskCard(
              title: task.title,
              description: task.description,
              subject: task.subject,
              deadline: task.deadline,
              isCompleted: task.isCompleted,
              priority: task.priority,
              isLoading: _togglingTasks.contains(task.id),
              onTap: () => _showTaskDetails(context, task),
              onToggleComplete: () => _toggleTaskCompletion(task),
              onEdit: () => _showEditTaskDialog(context, task),
              onDelete: () => _showDeleteTaskDialog(context, task),
            ),
          );
        },
      ),
    );
  }

  List<TaskModel> _getFilteredTasks(List<TaskModel> tasks) {
    print('üîç B·∫Øt ƒë·∫ßu filter tasks. T·ªïng s·ªë: ${tasks.length}');
    
    // Debug: In ra tr·∫°ng th√°i c·ªßa t·ª´ng task
    for (final task in tasks) {
      print('üìã Task "${task.title}" (ID: ${task.id}): isCompleted = ${task.isCompleted}');
    }
    
    // Debug: Ki·ªÉm tra duplicate tasks
    final duplicateTasks = <String, List<TaskModel>>{};
    for (final task in tasks) {
      duplicateTasks.putIfAbsent(task.title, () => []).add(task);
    }
    
    for (final entry in duplicateTasks.entries) {
      if (entry.value.length > 1) {
        print('‚ö†Ô∏è Duplicate tasks v·ªõi title "${entry.key}":');
        for (final task in entry.value) {
          print('  - ID: ${task.id}, isCompleted: ${task.isCompleted}');
        }
      }
    }
    
    List<TaskModel> filteredTasks = tasks;

    // Filter by completion status - m·∫∑c ƒë·ªãnh hi·ªÉn th·ªã t·∫•t c·∫£
    if (!_showCompleted) {
      final beforeFilter = filteredTasks.length;
      print('üîç B·∫Øt ƒë·∫ßu filter completed tasks...');
      print('üîç _showCompleted = $_showCompleted');
      print('üîç T·ªïng s·ªë tasks tr∆∞·ªõc filter: $beforeFilter');
      
      filteredTasks = filteredTasks.where((task) => !task.isCompleted).toList();
      print('‚úÖ Filter completed tasks: $beforeFilter ‚Üí ${filteredTasks.length} (·∫©n ${beforeFilter - filteredTasks.length} b√†i t·∫≠p ƒë√£ ho√†n th√†nh)');
      
      // Debug: In ra danh s√°ch task sau khi filter
      for (final task in filteredTasks) {
        print('‚úÖ Hi·ªÉn th·ªã task: "${task.title}" (isCompleted: ${task.isCompleted})');
      }
      
      if (filteredTasks.isEmpty) {
        print('‚ö†Ô∏è KH√îNG C√ì TASK N√ÄO HI·ªÇN TH·ªä! T·∫•t c·∫£ tasks ƒë·ªÅu ƒë√£ completed?');
        print('üîç Ki·ªÉm tra l·∫°i t·∫•t c·∫£ tasks:');
        for (final task in tasks) {
          print('  - "${task.title}": isCompleted = ${task.isCompleted}');
        }
      }
    } else {
      print('‚úÖ Hi·ªÉn th·ªã t·∫•t c·∫£ tasks (bao g·ªìm ƒë√£ ho√†n th√†nh)');
    }

    // Filter by subject
    if (_selectedSubject != 'all') {
      final beforeFilter = filteredTasks.length;
      print('üîç Debug subject filter:');
      print('  - Selected subject ID: $_selectedSubject');
      print('  - Available tasks:');
      for (final task in filteredTasks) {
        print('    - "${task.title}": subjectId = ${task.subjectId}, subject = ${task.subject}');
      }
      
      filteredTasks = filteredTasks.where((task) => task.subjectId == _selectedSubject).toList();
      print('üìö Filter by subject ID "$_selectedSubject": $beforeFilter ‚Üí ${filteredTasks.length}');
      
      if (filteredTasks.isEmpty) {
        print('‚ö†Ô∏è Kh√¥ng c√≥ task n√†o thu·ªôc subject ID: $_selectedSubject');
      }
    }

    // Filter by time
    final now = DateTime.now();
    switch (_selectedFilter) {
      case 'today':
        final beforeFilter = filteredTasks.length;
        final today = DateTime(now.year, now.month, now.day);
        final tomorrow = today.add(const Duration(days: 1));
        filteredTasks = filteredTasks.where((task) =>
          task.deadline.isAfter(today) && task.deadline.isBefore(tomorrow)
        ).toList();
        print('‚è∞ Filter today: $beforeFilter ‚Üí ${filteredTasks.length}');
        break;
      case 'week':
        final beforeFilter = filteredTasks.length;
        final weekStart = now.subtract(Duration(days: now.weekday - 1));
        final weekEnd = weekStart.add(const Duration(days: 7));
        filteredTasks = filteredTasks.where((task) =>
          task.deadline.isAfter(weekStart) && task.deadline.isBefore(weekEnd)
        ).toList();
        print('‚è∞ Filter week: $beforeFilter ‚Üí ${filteredTasks.length}');
        break;
      case 'month':
        final beforeFilter = filteredTasks.length;
        final monthStart = DateTime(now.year, now.month, 1);
        final monthEnd = DateTime(now.year, now.month + 1, 0);
        filteredTasks = filteredTasks.where((task) =>
          task.deadline.isAfter(monthStart) && task.deadline.isBefore(monthEnd)
        ).toList();
        print('‚è∞ Filter month: $beforeFilter ‚Üí ${filteredTasks.length}');
        break;
      default:
        print('‚è∞ Kh√¥ng filter theo th·ªùi gian (all)');
    }

    // Sort tasks: overdue first, then by deadline, then by priority
    print('üìä B·∫Øt ƒë·∫ßu sort tasks...');
    filteredTasks.sort((a, b) {
      final now = DateTime.now();
      final aOverdue = !a.isCompleted && a.deadline.isBefore(now);
      final bOverdue = !b.isCompleted && b.deadline.isBefore(now);
      
      // Overdue tasks first
      if (aOverdue && !bOverdue) return -1;
      if (!aOverdue && bOverdue) return 1;
      
      // Then by deadline (earliest first)
      final deadlineComparison = a.deadline.compareTo(b.deadline);
      if (deadlineComparison != 0) return deadlineComparison;
      
      // Then by priority (highest first)
      return a.priority.compareTo(b.priority);
    });
    print('‚úÖ Sort tasks ho√†n th√†nh. K·∫øt qu·∫£ cu·ªëi: ${filteredTasks.length} tasks');

    return filteredTasks;
  }

